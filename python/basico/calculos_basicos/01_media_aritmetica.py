# ==================================================
# Exercício: Média Aritmética
# Objetivo: Ler duas notas e calcular a média
# ==================================================

# Entrada de dados
nota1 = float(input('Digite a primeira nota: '))
nota2 = float(input('Digite a segunda nota: '))

# Processamento
media = (nota1 + nota2) / 2

# Saída de dados
print(f'A média das notas é {media:.2f}')
